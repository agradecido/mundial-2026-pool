export default function BottomTabBar() {
    const pathname = usePathname();
    const router = useRouter();
    const [mounted, setMounted] = useState(false);

    useEffect(() => {
        setMounted(true);
    }, []);

    return (
        <nav className="sm:hidden fixed bottom-0 inset-x-0 z-50 border-t border-white/[0.07] bg-black/70 backdrop-blur-xl">
            {/* top accent line */}
            <div className="absolute inset-x-0 top-0 h-px bg-gradient-to-r from-transparent via-[#00e87a]/30 to-transparent" />
            <div className="flex items-stretch justify-around pb-safe">
                {tabs.map(({ href, label, icon, activeIcon }) => {
                    const isActive = mounted && (pathname === href || pathname.startsWith(href + "/"));
                    return (
                        <Link
                            key={href}
                            href={href}
                            onClick={(e) => {
                                if (isActive) {
                                    e.preventDefault();
                                    router.push(href);
                                }
                            }}
                            className={`flex flex-col items-center justify-center gap-1 flex-1 py-2.5 transition-colors [touch-action:manipulation] ${isActive ? "text-[#00e87a]" : "text-gray-600 active:text-gray-300"
                                }`}
                        >
                            <span className="relative inline-flex">
                                {isActive ? activeIcon : icon}
                                <LinkSpinner className="absolute inset-0 m-auto size-5 text-[#00e87a]" />
                            </span>
                            <span className={`text-[10px] font-medium leading-none ${isActive ? "text-[#00e87a]" : "text-gray-600"}`}>
                                {label}
                            </span>
                            {isActive && (
                                <span className="absolute bottom-0 w-8 h-0.5 rounded-full bg-[#00e87a]/60" />
                            )}
                        </Link>
                    );
                })}
            </div>
        </nav>
    );
}
